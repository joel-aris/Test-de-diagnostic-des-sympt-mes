      <?php
class Lobiko {
    public $conditions;
    public $diagnostic;
    public $urgence;
    public $specificite;
    public $score;

    public function __construct($conditions, $diagnostic, $urgence = 0, $specificite = 0) {
        $this->conditions = $conditions;
        $this->diagnostic = $diagnostic;
        $this->urgence = $urgence;
        $this->specificite = $specificite;
        $this->score = $urgence + $specificite;
    }

    public function conditionsSatisfaites($faits) {
        foreach ($this->conditions as $condition) {
            if (empty($faits[$condition])) {
                return false;
            }
        }
        return true;
    }
}

function calculerNiveauSymptomes($faits) {
    $nbSymptomes = array_sum($faits);
    if ($nbSymptomes == 0) {
        return "Aucun symptôme détecté. Vous semblez en bonne santé.";
    } elseif ($nbSymptomes <= 2) {
        return "Symptômes légers. Pas de gravité apparente.";
    } elseif ($nbSymptomes <= 4) {
        return "Symptômes modérés. Une consultation médicale est recommandée.";
    } else {
        return "Symptômes graves. Consultez un médecin immédiatement.";
    }
}

function diagnostiquer($faits, $regles) {
    $reglesApplicables = array_filter($regles, function($regle) use ($faits) {
        return $regle->conditionsSatisfaites($faits);
    });

    if (empty($reglesApplicables)) {
        $niveauSymptomes = calculerNiveauSymptomes($faits);
        return "Aucune règle applicable. $niveauSymptomes";
    }

    usort($reglesApplicables, function($a, $b) {
        return $b->score <=> $a->score;
    });

    $reglePrioritaire = $reglesApplicables[0];
    return "Diagnostic : {$reglePrioritaire->diagnostic}";
}

